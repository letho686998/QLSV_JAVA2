    private void fillToTable() {
        model.setRowCount(0);
        for (Sinhvien sv : listSv) {
            model.addRow(new Object[]{sv.getId(), sv.getName(), sv.getGioitinh(), sv.getMarkAvg(), sv.getStatus(sv.getMarkAvg())});
        }
    }

    private void showDetail() {
        Sinhvien sv = listSv.get(index);
        txtmasinhvien.setText(sv.getId());
        txtHoten.setText(sv.getName());
        txtDiemTB.setText(sv.getMarkAvg() + "");
        String gioitinh;
        if (rdoNam.isSelected()) {
            gioitinh = "Nam";
        } else {
            gioitinh = "nữ";
        }

        tblSinhvien.setRowSelectionInterval(index, index);
        lblHientai.setText(index + 1 + "/" + listSv.size());
    }

