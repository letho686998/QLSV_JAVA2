    private void btnGhiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhiActionPerformed
        if (txtmasinhvien.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "mã sinh viên không được rỗng");
            txtmasinhvien.requestFocus();
            return;
        }
        if (txtHoten.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Họ tên không được rỗng");
            txtHoten.requestFocus();
            return;
        }
        if (txtDiemTB.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Điểm không được rỗng");
            txtDiemTB.requestFocus();
            return;
        }
        double diem;
        try {
            diem = Double.parseDouble(txtDiemTB.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Điểm phải là số");
            txtDiemTB.requestFocus();
            return;
        }
        if (diem < 0 || diem > 10) {
            JOptionPane.showMessageDialog(this, "Điểm phải nằm trong miền điểm");
            txtDiemTB.requestFocus();
            return;
        }
        String id = txtmasinhvien.getText();
        String hoten = txtHoten.getText();
        String gioitinh;
        if (rdoNam.isSelected()) {
            gioitinh = "Nam";
        } else {
            gioitinh = "nữ";
        }
        Sinhvien sv = new Sinhvien(id, hoten, gioitinh, diem);
        listSv.add(sv);
        model.addRow(new Object[]{sv.getId(), sv.getName(), sv.getGioitinh(), sv.getMarkAvg(), sv.getStatus(diem)});

    }//GEN-LAST:event_btnGhiActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Ban co muon xoa khong");
        if (hoi != JOptionPane.YES_OPTION) {
            return;
        }
        listSv.remove(index);
        model.removeRow(index);
        if (listSv.size() == 0) {
            clearForm();
        } else {
            if (listSv.size() == index) {
                index--;

            }
            showDetail();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnGhifileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhifileActionPerformed
        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("txt.doc"));
            oos.writeObject(listSv);
            oos.flush();

            oos.close();

            JOptionPane.showMessageDialog(this, " ghi file oke");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "lỗi ghi file");
            return;
        }
    }//GEN-LAST:event_btnGhifileActionPerformed

    private void btnDocfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocfileActionPerformed
        try {
            ObjectInputStream ois = new ObjectInputStream(new FileInputStream("txt.doc"));
            listSv = (ArrayList<Sinhvien>) ois.readObject();
            ois.close();
            if (listSv.size() > 0) {
                index = 0;
                showDetail();
            } else {
                clearForm();
            }
            JOptionPane.showMessageDialog(this, " đọc file oke");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "lỗi đọc file");
            return;
        }
    }//GEN-LAST:event_btnDocfileActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String id = txtmasinhvien.getText();
        String hoten = txtHoten.getText();
        String gioitinh;
        if (rdoNam.isSelected()) {
            gioitinh = "Nam";
        } else {
            gioitinh = "nữ";
        }
        double diem = Double.parseDouble(txtDiemTB.getText());
        Sinhvien sv = new Sinhvien(id, hoten, gioitinh, diem);
        index = tblSinhvien.getSelectedRow();
        listSv.set(index, sv);
        model.setValueAt(id, index, 0);
        model.setValueAt(hoten, index, 1);
        model.setValueAt(gioitinh, index, 2);
        model.setValueAt(diem, index, 3);

        if (listSv.size() > 0) {
            fillToTable();
            index = 0;
            showDetail();
        } else {
            clearForm();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

