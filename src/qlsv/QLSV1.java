    private void dongHo() {
        Thread t = new Thread() {
            @Override
            public void run() {
                SimpleDateFormat sdf = new SimpleDateFormat("hh:mm:ss aa");
                while (true) {
                    Date d = new Date();
                    lblThoigian.setText(sdf.format(d));
                    try {
                        Thread.sleep(1000);
                    } catch (Exception e) {
                    }
                }
            }

        };
        t.start();

    }

    private void getTime() {
        Thread t = new Thread() {
            @Override
            public void run() {
                int count = 0;
                int phut = 0;
                int gio = 0;
                while (true) {
                    try {
                        lblGio.setText(gio + ":" + phut + ":" + count);
                        Thread.sleep(1000);
                        count++;
                        if (count == 60) {
                            phut++;
                            count = 1;
                            lblGio.setText(gio + ":" + phut + ":" + count);
                            count++;
                            if (phut == 60) {
                                gio++;
                                count = 1;
                                phut = 0;
                                lblGio.setText(gio + ":" + phut + ":" + count);
                                count++;
                            }
                        }
                    } catch (Exception e) {
                        break;
                    }
                }
            }

        };
        t.start();
    }

